			<h2>What is a postpartum doula?</h2>
			<p>There are various options you can choose for assisted care for your newborn. It is important as a new mother that you understand these available levels of care and 
			what you should expect from each.</p> 
			<p><a href="#" id="readmore2" class="link-style">Read More</a></p>
        	<p id="more2" style="display:none;">A baby nurse is typically someone who has earned a degree in nursing, however the term <b>baby nurse</b> has been used for non-medical 
			professionals which can cause confusion for the families seeking professional assistance. The term that is used now for non-medical help is <b>newborn care specialist</b>. 
			A newborn care specialist may or may not have certification in their field. Generally, they will have experience working with newborns and babies. The main goal of a 
			newborn care specialist is the overall care of the infant; they will tend solely to the infant as a caregiver allowing mom and dad to get their much needed rest.<br/>
        	
			A <b>postpartum doula</b> on the other hand is someone that is certified or working towards certification specifically with a focus on the new mother and the first 12-16 weeks 
			of the infant's life. A postpartum doula (our service) supports the mother and provides education in the care of her newborn. We work in conjunction with the mother 
			to help ease the transition of bringing home a new baby or babies. A postpartum doula cares for all aspects of your family. The primary focus is on the mother 
			and her needs after delivery, and we also are experts in what to expect and how to prepare for the next 12-16 weeks of the new infant's life.
